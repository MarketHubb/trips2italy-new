                            <h3>Embark to medieval fortress from the 14th century</h3>
                            <p>After a leisurely breakfast, set out to explore the medieval fortress of Rocca Maggiore
                                and some of the local churches. Rocca Maggiore is a fourteenth century castle that
                                stands tall at the very highest part of the city and offers stunning views of Assisi,
                                Perugia and beyond. The area is also home to a number of historical churches that are
                                worth a visit including the famous Basilica of St. Francis, Santa Maria Sopra Minerva,
                                Santa Maria degli Angeli, Duomo di San Rufino, Chiesa Nuova, San Damiano, and the
                                Basilica di Santa Chiara.</p>
