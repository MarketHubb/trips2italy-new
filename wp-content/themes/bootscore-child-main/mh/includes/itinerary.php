                        <div class="wptl-excerpt">
                            <h3>Explore St. Peter’s Basilica</h3>
                            <p>Breakout your best clothes for today as you and your love will be visiting the Vatican.
                                This religious complex sometimes referred to as one of the world’s smallest countries,
                                is packed with internationally recognized art and history inside the walls of St.
                                Peter’s Basilica, on St. Peter’s Square, and in the Vatican museums and Sistine Chapel.
                                A visit to the Vatican is said by religious and nonreligious visitors alike to be a very
                                moving experience. If you are visiting on a Wednesday when the Pope is in Rome, you may
                                even be able to attend a Papal Audience at the Vatican. Traditionally the Pope appears
                                to visitors and gives a Papal Blessing. This will certainly be a very special
                                memory.</p>
