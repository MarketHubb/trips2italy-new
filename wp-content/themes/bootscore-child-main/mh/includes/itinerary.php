                        <div class="wptl-more-meta"><span>Rome</span></div>
                        <div class="wptl-excerpt">
                            <h3>Your romantic adventure begins</h3>
                            <p>As you step out of the airport, take a deep breath and let the magic of Italy snare your
                                senses. After a ride to your lodging for the next few days, opt to unpack later and
                                instead take advantage of the daylight and explore the city. A few landmarks of Rome you
                                will not want to miss include the Borghese Gallery which is home to an extensive private
