                            <h3>Voyage to the Venetian islands</h3>
                            <p>Today is a day of choices between an excursion to the Venetian islands of Murano, Burano,
                                and Torcello or an excursion to nearby Verona. A trip to Murano, Burano, and Torcello
                                will yield glassblowing and lace making demonstrations that make fabulous trip souvenirs
