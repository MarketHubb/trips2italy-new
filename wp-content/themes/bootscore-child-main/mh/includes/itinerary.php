                            <h3>An enchanting gondola ride awaits</h3>
                            <p>Start your day with a visit to the beautiful St. Mark’s Basilica, one of the most visited
                                churches in Venice. The church is sometimes nicknamed the Church of Gold because of the
                                Byzantine glass mosaics that cover much of the inside and outside. After you’ve overcome
                                the pleasant surprise of roughly eighty-five thousand square feet of mosaics inside the
                                church, it is time for one of the most time honored traditions of Venice, a gondola
                                ride. Sail through the canals of the city with your gondola operator providing a brief
                                history of important landmarks and serenading you with a special Italian love song.</p>
