                            <h3>Trace the steps of gladiators at the colosseum</h3>
                            <p>Today you will be visiting one of the most iconic symbols of Rome, the Roman Colosseum.
                                This dramatic structure traces centuries back to the early Roman period. These ruins are
                                still intact enough that to stand in the middle of the arena and picture a crowd going
                                wild as a gladiator and his opponent step into the ring to battle for survival feels
                                eerily realistic. A guided tour will also provide a visit to the underground Colosseum
                                where you can virtually trace the steps of gladiators as they waited for their turn in
                                the spotlight.</p>
