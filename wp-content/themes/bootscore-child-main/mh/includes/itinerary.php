                        <div class="wptl-excerpt">
                            <h3>Lose yourself in the piazzas</h3>
                            <p>Rome is large city filled with historic and architectural treasures that cannot hardly be
                                seen in the space of one day, much less three. So today, explore the city at your
                                leisure making sure to stop at the Fontana di Trevi, or Trevi Fountain, to witness one
                                of the most beautiful fountains in Europe that features a number of stunning statues
                                that give the fountain a regal touch. Next wander through Piazza Navona, a famous public
                                square featured in many films including Dan Brown’s Angels &amp;amp; Demons which is
                                fabulous for people watching. If time allows, be sure to visit the famous Spanish Steps
                                adjacent to the Piazza di Spagna and take in the sunset at the majestic Vitoriano.</p>
